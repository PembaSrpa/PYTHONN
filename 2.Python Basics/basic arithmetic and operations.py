# Basic Arithmetic and Operators

# Basic Arithmetic Operators

# Addition (+)
result_add = 2 + 3
print("Addition: 2 + 3 =", result_add)  # Output: 5

# Subtraction (-)
result_sub = 7 - 4
print("Subtraction: 7 - 4 =", result_sub)  # Output: 3

# Multiplication (*)
result_mul = 5 * 6
print("Multiplication: 5 * 6 =", result_mul)  # Output: 30

# Division (/)
result_div = 8 / 2
print("Division: 8 / 2 =", result_div)  # Output: 4.0

# Floor Division (//)
result_floordiv = 9 // 2
print("Floor Division: 9 // 2 =", result_floordiv)  # Output: 4

# Modulus (%)
result_mod = 10 % 3
print("Modulus: 10 % 3 =", result_mod)  # Output: 1

# Exponentiation (**)
result_exp = 2 ** 4
print("Exponentiation: 2 ** 4 =", result_exp)  # Output: 16

