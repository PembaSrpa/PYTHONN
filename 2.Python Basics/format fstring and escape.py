# Working with format() method, f-strings, & escape sequences

name = "John"

# The format() method is used to format strings.
print("Hello, {}!".format(name))

# The f-string is used to format strings.
print(f"Hello, {name}!")

# The escape sequence is used to escape special characters.
print("Hello, \"World\"!")

# The newline escape sequence is used to add a newline to a string.
print("Hello, \nWorld!")