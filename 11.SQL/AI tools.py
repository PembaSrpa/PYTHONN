# DBeaver: A Powerful Tool for Executing and Managing SQL Queries
# DBeaver is a popular, open-source database management tool that provides a graphical interface for running and visualizing SQL queries, managing database schemas, and browsing data. It supports a wide range of databases, including MySQL, PostgreSQL, SQLite, and more. DBeaver enables efficient query execution, result visualization, export/import operations, and advanced features like ER diagrams and data comparison.
# DBeaver Examples

# Example 1: Connecting to a MySQL database in DBeaver
#  - Open DBeaver, click on "New Database Connection"
#  - Select "MySQL", fill in host, port, user, password, and database, then click "Finish"

# Example 2: Running a Basic SELECT Query
#  - Open the SQL Editor tab connected to your database
#  - Type and execute:
#    SELECT * FROM employees;

# Example 3: Filtering Data with WHERE clause
#    SELECT name, department FROM employees WHERE age > 30;

# Example 4: Exporting Query Results
#  - After running a query and viewing results, right-click on the results grid
#  - Choose "Export Resultset", select format (CSV, Excel, etc.), and complete export wizard

# Example 5: Visualizing Table Relationships
#  - In the database navigator, right-click on a table and select "ER Diagram"
#  - Review and edit relationships between tables graphically


